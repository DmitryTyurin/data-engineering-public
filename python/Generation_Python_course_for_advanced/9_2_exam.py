print(one if one else "NO")


"""–†—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª—é –æ–Ω–ª–∞–π–Ω-—à–∫–æ–ª—ã BEEGEEK –∑–∞—Ö–æ—Ç–µ–ª–æ—Å—å —É–∑–Ω–∞—Ç—å, –∫—Ç–æ –∏–∑ –µ–≥–æ —É—á–µ–Ω–∏–∫–æ–≤ –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–æ–≤–∞–ª –Ω–∞ –≤—Å–µ—Ö —É—Ä–æ–∫–∞—Ö —Å –Ω–∞—á–∞–ª–∞
—É—á–µ–±–Ω–æ–≥–æ –≥–æ–¥–∞. –î–ª—è –∫–∞–∂–¥–æ–≥–æ —É—Ä–æ–∫–∞ –µ—Å—Ç—å –ª–∏—Å—Ç–æ–∫ —Å–æ —Å–ø–∏—Å–∫–æ–º –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–æ–≤–∞–≤—à–∏—Ö —É—á–µ–Ω–∏–∫–æ–≤.
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –æ–ø—Ä–µ–¥–µ–ª—è—é—â—É—é —Ñ–∞–º–∏–ª–∏–∏ —É—á–µ–Ω–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–æ–≤–∞–ª–∏ –Ω–∞ –≤—Å–µ—Ö —É—Ä–æ–∫–∞—Ö."""

